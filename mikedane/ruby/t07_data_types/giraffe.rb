name = "Giraffe Academy"        # string
puts 'Name   = "' + name + '"'

age = 75                        # integer number
print 'Age    = '
puts age

gpa = -3.2                      # floating-point number
print 'GPA    = '
puts gpa

is_male	= true                  # boolean
print 'isMale = '
puts is_male

is_tall	= false                 # boolean
print 'isTall = '
puts is_tall

flaws = nil                     # no value			
print 'Flaws  = '
puts flaws
