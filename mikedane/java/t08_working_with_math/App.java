public class App
{
	public static void main(String[] args)
	{
		System.out.println( 3 );
		System.out.println( 3 + 4 );
		System.out.println( 3 - 4 );
		System.out.println( 3 * 4 );
		System.out.println( 3 / 4 );
		System.out.println( 3.0 / 4.0 );
		System.out.println( 10 % 3 );
	}
}
