public class App
{
	public static void main(String[] args)
	{
		System.out.println("Giraffe Academy");
		System.out.println("Giraffe \nAcademy");
		System.out.println("Giraffe \"Academy\"");
		System.out.println("Giraffe \\ Academy");

		System.out.println("Giraffe Academy" +
			"asdfa sadf " +
			"asdfsadf");
	}
}
